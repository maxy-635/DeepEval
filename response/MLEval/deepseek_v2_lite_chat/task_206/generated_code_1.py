def method(input_dict):
    """
    Function to count the number of key-value pairs in a dictionary.
    
    Parameters:
    input_dict (dict): The input dictionary to count the key-value pairs of.
    
    Returns:
    int: The number of key-value pairs in the input dictionary.
    """
    return len(input_dict)

